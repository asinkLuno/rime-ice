-- 日文假名输入
-- 输入 x + 拼音可得到平假名和片假名
-- 例如：ka -> か、カ　　shi -> し、シ

local yield = yield
local Candidate = Candidate

local M = {}

function M.init(env)
    env.name_space = env.name_space:gsub('^*', '')
    local config = env.engine.schema.config
    env.prefix = config:get_string(env.name_space .. '/prefix') or 'x'
end

